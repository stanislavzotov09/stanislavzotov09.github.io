function log_me($message) {
	  if (WP_DEBUG === true) {
		  if (is_array($message) || is_object($message)) {
			  error_log(print_r($message, true));
		  } else {
			  error_log($message);
		  }
	  }
}

function basic_template_redirect() {
	global $post, $wp_query;
	// gets the parent's template name of the page
	$template_name = get_post_meta( $post->post_parent, '_wp_page_template', true );
	// gets the template name of the page
	$post_name =  get_page_template_slug();
		$post_name = str_replace("template-parts/", "", $post_name);
		get_template_part( 'template-parts/page', $post_name);

	log_me("R: " . $post->post_parent . " T: " . $template_name);
	// the array of parent's template pages
	$redirect_pages = array("page-landingpage-base.php", "page-landingpage-base-header.php");
	// if parent's page is in the array, but the page itself is not in the array, then redirect
	if(in_array($template_name, $redirect_pages) && ! in_array($post_name, $redirect_pages))
	{
		log_me("R: Redirected");
		wp_redirect( get_permalink($post->post_parent), 301 );
		exit();
	}
}
add_action( 'template_redirect', 'basic_template_redirect' );


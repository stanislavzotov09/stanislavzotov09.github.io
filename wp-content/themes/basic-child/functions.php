// Rearrange the css files load order
function my_theme_enqueue_styles() {

	// loads the underscore base style.css file
	wp_enqueue_style( 'wordpress-style', get_template_directory_uri() . '/style.css' );
	// loads fontcustom
	wp_enqueue_style( 'child-fontcustom-style',
		get_stylesheet_directory_uri() . '/_/css/fontcustom/fontcustom.css'
	);
	// loads the basic-child template css file
	wp_enqueue_style( 'child-custom-style',
		get_stylesheet_directory_uri() . '/_/css/site-child.min.css'
	);

	// loads the child template css file
	wp_enqueue_style( 'child-style',
		get_stylesheet_directory_uri() . '/style.css'
	);
}
add_action( 'wp_enqueue_scripts', 'my_theme_enqueue_styles' );

// Making jQuery to load from Google Library
function my_theme_enqueue_scripts() {

	if (!is_admin()) {
		// comment out the next two lines to load the local copy of jQuery
		wp_deregister_script('jquery');
		wp_register_script('jquery', 'https://ajax.googleapis.com/ajax/libs/jquery/3.1.1/jquery.min.js', false, '3.1.1');
		wp_enqueue_script('jquery');
		wp_register_script('slider', 'https://unpkg.com/flickity@2/dist/flickity.pkgd.js', false);
		wp_enqueue_script('slider');
		wp_register_script('aos', 'https://cdn.rawgit.com/michalsnik/aos/2.1.1/dist/aos.js');
		wp_enqueue_script('aos');
	}
	// Load custom scripts
    wp_enqueue_script( 'child-script', get_stylesheet_directory_uri() . '/_/js/functions.js', true );

}
add_action( 'wp_enqueue_scripts', 'my_theme_enqueue_scripts' );


function sdt_remove_ver_css_js( $src, $handle ) {
    if ( strpos( $src, 'ver=' ) &&  strpos( $src, 'flickity.pkgd' ) )
        $src = remove_query_arg( 'ver', $src );

    return $src;
}
add_filter( 'style_loader_src',  'sdt_remove_ver_css_js', 9999, 2 );
add_filter( 'script_loader_src', 'sdt_remove_ver_css_js', 9999, 2 );


